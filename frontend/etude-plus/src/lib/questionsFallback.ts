/**
 * Local question fallback — used when the remote API returns no questions.
 *
 * Key format:
 *   Simple levels:        "${levelCode}/${subject}/${chapterName}"
 *   Section levels:       "${levelCode}/${sectionKey}/${subject}/${chapterName}"
 *
 * Chapter names MUST match curriculumFallback.ts exactly.
 * IDs are negative to distinguish from real DB rows.
 *
 * Schema:
 *   - multiple-choice: instruction + question + options[A..D] + correctAnswer (letter) + explanation
 *   - problem-solving: instruction + question + correctAnswer (text) + explanation
 *   - short-answer:    instruction + question + correctAnswer (text) + explanation
 *
 * Student-facing UI shows: instruction → question → options → points.
 * correctAnswer and explanation are hidden until the student reveals the mark scheme.
 */

export type FallbackQuestionType = "multiple-choice" | "problem-solving" | "short-answer";

export interface FallbackOption {
  label: "A" | "B" | "C" | "D";
  text:  string;
}

export interface FallbackQuestion {
  id:                   number;
  type:                 FallbackQuestionType;
  instruction:          string;
  question:             string;
  options?:             FallbackOption[];
  correctAnswer:        string;
  explanation:          string;
  difficulty:           "facile" | "moyen" | "difficile";
  totalMarks:           number;
  estimatedTimeMinutes: number;
  requiresCalculator:   boolean;
  direction:            "ltr" | "rtl";
  source:               "manual-starter" | "manual-starter-2eme";
}

// ──────────────────────────────────────────────────────────────────────────────
// Helpers
// ──────────────────────────────────────────────────────────────────────────────

function mcq(
  id: number,
  question: string,
  options: [string, string, string, string],
  correctAnswer: "A" | "B" | "C" | "D",
  explanation: string,
  opts: { difficulty?: FallbackQuestion["difficulty"]; marks?: number; minutes?: number; calc?: boolean; rtl?: boolean } = {},
): FallbackQuestion {
  const rtl = opts.rtl ?? false;
  return {
    id,
    type: "multiple-choice",
    instruction: rtl ? "اختر الإجابة الصحيحة." : "a) Choisissez la bonne réponse.",
    question,
    options: [
      { label: "A", text: options[0] },
      { label: "B", text: options[1] },
      { label: "C", text: options[2] },
      { label: "D", text: options[3] },
    ],
    correctAnswer,
    explanation,
    difficulty: opts.difficulty ?? "facile",
    totalMarks: opts.marks ?? 2,
    estimatedTimeMinutes: opts.minutes ?? 3,
    requiresCalculator: opts.calc ?? false,
    direction: rtl ? "rtl" : "ltr",
    source: "manual-starter",
  };
}

function problem(
  id: number,
  question: string,
  correctAnswer: string,
  explanation: string,
  opts: { difficulty?: FallbackQuestion["difficulty"]; marks?: number; minutes?: number; calc?: boolean; rtl?: boolean } = {},
): FallbackQuestion {
  const rtl = opts.rtl ?? false;
  return {
    id,
    type: "problem-solving",
    instruction: rtl ? "حلّ المسألة التالية." : "Résolvez le problème suivant.",
    question,
    correctAnswer,
    explanation,
    difficulty: opts.difficulty ?? "moyen",
    totalMarks: opts.marks ?? 3,
    estimatedTimeMinutes: opts.minutes ?? 5,
    requiresCalculator: opts.calc ?? false,
    direction: rtl ? "rtl" : "ltr",
    source: "manual-starter",
  };
}

function shortAnswer(
  id: number,
  question: string,
  correctAnswer: string,
  explanation: string,
  opts: { difficulty?: FallbackQuestion["difficulty"]; marks?: number; minutes?: number; rtl?: boolean } = {},
): FallbackQuestion {
  const rtl = opts.rtl ?? false;
  return {
    id,
    type: "short-answer",
    instruction: rtl ? "أجب عن السؤال التالي." : "Répondez brièvement.",
    question,
    correctAnswer,
    explanation,
    difficulty: opts.difficulty ?? "facile",
    totalMarks: opts.marks ?? 2,
    estimatedTimeMinutes: opts.minutes ?? 3,
    requiresCalculator: false,
    direction: rtl ? "rtl" : "ltr",
    source: "manual-starter",
  };
}

