export function getFallbackQuestions(
  levelCode: string,
  subject: string,
  topic: string,
  sectionKey?: string | null,
): FallbackQuestion[] {
  if (sectionKey) {
    const key = `${levelCode}/${sectionKey}/${subject}/${topic}`;
    if (QUESTIONS_FALLBACK[key]) return QUESTIONS_FALLBACK[key];
  }
  return QUESTIONS_FALLBACK[`${levelCode}/${subject}/${topic}`] ?? [];
}

export function getQualitativeFeedback(score: number, total: number): string {
  if (total === 0) return "Aucune question évaluée.";
  const pct = score / total;
  if (pct >= 0.85) return "Très bon travail ! Vous maîtrisez bien ce chapitre.";
  if (pct >= 0.65) return "Bon travail. Quelques notions sont encore à renforcer.";
  if (pct >= 0.40) return "Résultats mitigés. Nous vous conseillons de refaire quelques exercices.";
  return "Vous devriez réviser davantage ce chapitre avant de continuer.";
}
